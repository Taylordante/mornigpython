#Arithmetic Operators
X = 20
Y = 6

print(X + Y)
print(X - Y)
print(X * Y)
print(X / Y)
print(X % Y)#Modulus- Returns remainder after Division

#Comparison Operation - Comparing values
print(X < Y)
print(X > Y)
print(X <= Y)
print(X >= Y)
print(X == Y) #Equal to
print(X != Y)

#Assigment Operators - Used to assign/store values in valuables
number = 45
print(number)

number += 2
print(number)


#Logical Operators - and, or, not

num1 = 67
num2 = 43

print(num1 == num2 and num1> num2)
print(not(num1 == num2 or num1> num2))
















